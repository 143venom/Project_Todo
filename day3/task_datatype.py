# Integer:
number = 20
print(type(number))

# Float:
decimal = 20.20
print(type(decimal))

# String:
first = "Hello, World!"
print(type(first))

# Boolean:
value = True
print(type(value))

# List:
fruits = ["apple", "banana", "orange"]
print(type(fruits))

# Tuple:
point = (2, 4)
print(type(point))

# Set:
numbers = {1, 2, 3, 4, 5}
print(type(numbers))

# Dictionary:
person = {"name": "John", "age": 25, "city": "New York"}
print(type(person))

#None:
result = None
print(type(result))
